#include <libft.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*str;
	int		i;

	i = 0;
	str = s1;
	while (s1[i])
		s1++;
	while (s2[i] && (int)n-- > 0)
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (str);
}
