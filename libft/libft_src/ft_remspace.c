#include <libft.h>

int		ft_remspace(const char *str, int c)
{
	if (c == 0)
	{
		while (str[c] == ' ' || str[c] == '\t' || str[c] == '\n')
			c++;
		return (c);
	}
	else
	{
		while (str[c] == ' ' || str[c] == '\t' || str[c] == '\n'
				|| str[c] == '\0')
			c--;
		return (c);
	}
}
